    def work(self):
        self.a=int(self.lineEdit.text())
        self.b=int(self.l2.text())
        self.c=int(self.l3.text())
        self.d=(self.b)**2-4*(self.a)*(self.c)
        self.d1=(self.d)**(1/2)
        self.d2=cmath.sqrt(self.d)
        if self.d==0:
            self.x1=-self.b/2*self.a
            self.x2=-self.b/2*self.a
            self.l4.setText("both are "+str(self.x2))
        elif self.d>0:
            self.x1=(-(self.b)+(self.d1))/(2*self.a)
            self.x2=(-(self.b)-(self.d1))/(2*self.a)
            self.k=round(self.x1,4)
            self.w=round(self.x2,4)
            self.l4.setText(str(self.k)+" and "+str(self.w))
        else:
            self.x1=(-(self.b)+(self.d1))/(2*self.a)
            self.x2=(-(self.b)-(self.d1))/(2*self.a)
            self.k='{0.real:.2f}+({0.imag:.2f})i'.format(self.x1)
            self.w='{0.real:.2f}+({0.imag:.2f})i'.format(self.x2)
            self.l4.setText(str(self.k)+" and "+str(self.w))
            
             
